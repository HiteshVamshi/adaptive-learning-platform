def _question(
    *,
    question_id: str,
    chapter_id: str,
    chapter_name: str,
    concept_id: str,
    concept_name: str,
    difficulty: str,
    question_type: str,
    prompt: str,
    answer: str,
    worked_solution: str,
    tags: list[str],
    estimated_time_sec: int,
    explanation_style: str = "stepwise",
) -> tuple[QuestionRecord, SolutionRecord]:
    return (
        QuestionRecord(
            question_id=question_id,
            chapter_id=chapter_id,
            chapter_name=chapter_name,
            concept_id=concept_id,
            concept_name=concept_name,
            difficulty=difficulty,
            question_type=question_type,
            prompt=prompt,
            answer=answer,
            tags=tags,
            estimated_time_sec=estimated_time_sec,
        ),
        SolutionRecord(
            question_id=question_id,
            final_answer=answer,
            worked_solution=worked_solution,
            explanation_style=explanation_style,
        ),
    )


